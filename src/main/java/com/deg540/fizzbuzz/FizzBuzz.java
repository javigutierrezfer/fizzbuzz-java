package com.deg540.fizzbuzz;

class FizzBuzz {

    public String convert(int number) {
        if (number == 3) {
            return "Fizz";
        }

        if (number == 6) {
            return "Fizz";
        }

        if (number == 9) {
            return "Fizz";
        }


        return String.valueOf(number);
    }
}
